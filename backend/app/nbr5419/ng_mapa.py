def total_municipios_cadastrados() -> int:
    """Retorna quantos municípios estão cadastrados na base."""
    return len(NG_POR_MUNICIPIO)


def consultar_NG_por_municipio(municipio_uf: str) -> float | None:
    """
    Retorna o NG oficial do município.

    Args:
        municipio_uf: Ex. "São Paulo-SP" (case-sensitive conforme Tabela F.1)

    Returns:
        NG em raios/km²/ano, ou None se não cadastrado.
    """
    return NG_POR_MUNICIPIO.get(municipio_uf)


def buscar_municipios(query: str, limit: int = 20, uf: str | None = None) -> list[dict]:
    """
    Busca municípios por nome (busca parcial, case-insensitive).

    Args:
        query: Texto a buscar (mínimo 2 caracteres)
        limit: Máximo de resultados
        uf: Filtrar por estado (ex: "SP") — opcional

    Returns:
        Lista de dicts com municipio_uf, NG, nome, uf
    """
    if len(query.strip()) < 2:
        return []

    query_lower = query.strip().lower()
    uf_filter = uf.strip().upper() if uf else None
    resultados: list[dict] = []
    for chave, ng in NG_POR_MUNICIPIO.items():
        if query_lower in chave.lower():
            partes = chave.rsplit("-", 1)
            mun_uf = partes[1].strip() if len(partes) == 2 else ""
            if uf_filter and mun_uf.upper() != uf_filter:
                continue
            resultados.append({
                "municipio_uf": chave,
                "NG": ng,
                "nome": partes[0] if len(partes) == 2 else chave,
                "uf": mun_uf,
            })
            if len(resultados) >= limit:
                break
    return resultados


